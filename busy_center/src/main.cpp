char hexChar(int i) {
  if(0<=i && i<=9)
    return '0'+i;
  if(10<=i && i<16)
    return 'a'+i-10;
  return '?';
}

KMqttClient     kmqtt;

int resetBoardWinState() {
  Wire.beginTransmission(0x3b);
  Wire.write('R');
  int err=Wire.endTransmission();
  return err;
}

void sendConf(byte *st) {

  int page=16;
  for(int k=0;k<256;k+=page) {
    Wire.beginTransmission(0x3b);
    Wire.write((k==0)?'!':'@');
    for(int i=0;i<page;i++)  {
      Wire.write(hexChar(st[k+i]&0xf));
    }
    int err=Wire.endTransmission();

    if(err>0) {
      Serial.print(F("i2c send error:"));
      Serial.println(err);
      Wire.begin(D1,D2);
      break;
    }
  }
}

Puzzle p;

void updateBusyLogic() {
  sendConf(p.state);

  if(Wire.requestFrom(0x3b,1) == 1) {
    int solved=Wire.read();
    if(solved=='S') {
      // FIXME: backward compat
      kmqtt.publishMetric(std::string("busy_board/state"),1111);
    } else {
      kmqtt.publishMetric(std::string("busy_board/state"),1234);
    }
  } else {
    Serial.println("error reading bb state!");
  }
}

